    public void listarProfessores() {
        persistencia = new PersistenciaJPA();
        persistencia.conexaoAberta();
        cmbProfessores.removeAllItems();
        List<Professores> lista = persistencia.getProfessores();
        for (Professores p : lista) {
            cmbProfessores.addItem(p);
        }
        persistencia.fecharConexao();
    }

