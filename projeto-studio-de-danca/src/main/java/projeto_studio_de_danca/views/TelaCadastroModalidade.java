    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {

            persistencia = new PersistenciaJPA();
            persistencia.conexaoAberta();

            // Busque a entidade gerenciada
            Modalidade modalidadeGerenciada = null;
            if (modalidade != null && modalidade.getId() != null) {
                modalidadeGerenciada = (Modalidade) persistencia.find(Modalidade.class, modalidade.getId());
            }

            if (modalidadeGerenciada == null) {
                modalidadeGerenciada = new Modalidade();
            }
            modalidadeGerenciada.setDescricao(descricao.getText());
            modalidadeGerenciada.setProfessor((Professores) cmbProfessores.getSelectedItem());

            persistencia.merge(modalidadeGerenciada);
            JOptionPane.showMessageDialog(this, "Modalidade salva com sucesso!");
            dispose();
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroModalidade.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            persistencia.fecharConexao();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

